# Simple variable concept in Python
name = "Harrish"          # string
age = 21                  # integer
height = 5.9              # float
is_student = True         # boolean

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

# Variables can be updated
age = age + 1
print("Updated age:", age)

# Python allows changing variable type
value = 100
print("Value:", value, "Type:", type(value))

value = "one hundred"
print("Value:", value, "Type:", type(value))
